package trees;

public class BinarySearchTree {
    public class Node {
        private int value;
       private int height;
       private Node left;
       private Node right;

       private Node(int value) {
           this.value = value ;
       }

        public int getValue() {
            return value;
        }
    }
    private Node root;
    public BinarySearchTree() {

    }
    public int height(Node node) {
        if (node == null) {
            return - 1;
        }
        return node.height;
    }
    public boolean isEmpty() {
        return root==null;
    }
}
